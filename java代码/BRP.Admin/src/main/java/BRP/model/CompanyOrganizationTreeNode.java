package BRP.model;

import java.util.List;

/**
 * 组织机构树节点
 */
public class CompanyOrganizationTreeNode {
    private Integer id;
    private Integer value;
    private Integer parentId;
    private String description;
    private Boolean disabled;
    private String label;
    private List<CompanyOrganizationTreeNode> children;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }

    public Integer getParentId() {
        return parentId;
    }

    public void setParentId(Integer parentId) {
        this.parentId = parentId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Boolean getDisabled() {
        return disabled;
    }

    public void setDisabled(Boolean disabled) {
        this.disabled = disabled;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public List<CompanyOrganizationTreeNode> getChildren() {
        return children;
    }

    public void setChildren(List<CompanyOrganizationTreeNode> children) {
        this.children = children;
    }

}
