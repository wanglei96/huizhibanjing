package BRP.data;

import java.util.List;

public class BorrowReturnBookInfo extends ViewMemberCard {
    private Integer borrowingBookCount;
    private Integer borrowCount;
    private Integer borrowedBookCount;
    private List<ViewBookOrderItem> borrowBookList;
    private List<ViewBookDamage> bookDamageList;
    private String birthdayText;
    private String expireDateText;
    private boolean canBorrowed;

    private String notBorrowedCause;

    public String getBirthdayText() {
        return birthdayText;
    }

    public void setBirthdayText(String birthdayText) {
        this.birthdayText = birthdayText;
    }

    public String getExpireDateText() {
        return expireDateText;
    }

    public void setExpireDateText(String expireDateText) {
        this.expireDateText = expireDateText;
    }

    public List<ViewBookOrderItem> getBorrowBookList() {
        return borrowBookList;
    }

    public void setBorrowBookList(List<ViewBookOrderItem> borrowBookList) {
        this.borrowBookList = borrowBookList;
    }

    public Integer getBorrowingBookCount() {
        return borrowingBookCount;
    }

    public void setBorrowingBookCount(Integer borrowingBookCount) {
        this.borrowingBookCount = borrowingBookCount;
    }

    public Integer getBorrowCount() {
        return borrowCount;
    }

    public void setBorrowCount(Integer borrowCount) {
        this.borrowCount = borrowCount;
    }

    public Integer getBorrowedBookCount() {
        return borrowedBookCount;
    }

    public void setBorrowedBookCount(Integer borrowedBookCount) {
        this.borrowedBookCount = borrowedBookCount;
    }

    public List<ViewBookDamage> getBookDamageList() {
        return bookDamageList;
    }

    public void setBookDamageList(List<ViewBookDamage> bookDamageList) {
        this.bookDamageList = bookDamageList;
    }

    public boolean getCanBorrowed() {
        return canBorrowed;
    }

    public void setCanBorrowed(boolean canBorrowed) {
        this.canBorrowed = canBorrowed;
    }

    public String getNotBorrowedCause() {
        return notBorrowedCause;
    }

    public void setNotBorrowedCause(String notBorrowedCause) {
        this.notBorrowedCause = notBorrowedCause;
    }
}
