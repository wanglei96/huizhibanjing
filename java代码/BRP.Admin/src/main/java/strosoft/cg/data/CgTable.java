package strosoft.cg.data;

import java.util.List;

import strosoft.app.data.DataEntity;
import strosoft.cg.common.SearchItemInfo;

public class CgTable  extends DataEntity{
	private String tableName;
	private String type;
	private List<CgTableColumn> tableColumns;
	private List<SearchItemInfo> searchItems;
	private String modulePath;//moduleDocument.replace(".", "/")
	private String dataSource;
	private String moduleName;
	private String moduleDocument;
	private String formattedTableName;
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public List<SearchItemInfo> getSearchItems() {
		return searchItems;
	}
	public void setSearchItems(List<SearchItemInfo> searchItems) {
		this.searchItems = searchItems;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getModuleDocument() {
		return moduleDocument;
	}
	public void setModuleDocument(String moduleDocument) {
		this.moduleDocument = moduleDocument;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public List<CgTableColumn> getTableColumns() {
		return tableColumns;
	}
	public void setTableColumns(List<CgTableColumn> tableColumns) {
		this.tableColumns = tableColumns;
	}
	public String getDataSource() {
		return dataSource;
	}
	public void setDataSource(String dataSource) {
		this.dataSource = dataSource;
	}
	public String getFormattedTableName() {
		return formattedTableName;
	}
	public void setFormattedTableName(String formattedTableName) {
		this.formattedTableName = formattedTableName;
	}
	public String getModulePath() {
		return modulePath;
	}
	public void setModulePath(String modulePath) {
		this.modulePath = modulePath;
	}
	
	
}
