package strosoft.cg.data;

import java.util.List;

import strosoft.app.data.DataEntity;

public class CgModule  extends DataEntity{
	private String moduleName;
	private String moduleDocument;
	private String dataSource;
	private String modulePath;//moduleDocument.replace(".", "/")
	private List<CgTable> tables;
	private List<CgView> views;
	private List<String> procedures;
	public List<CgTable> getTables() {
		return tables;
	}
	public void setTables(List<CgTable> tables) {
		this.tables = tables;
	}
	public List<CgView> getViews() {
		return views;
	}
	public void setViews(List<CgView> views) {
		this.views = views;
	}
	public List<String> getProcedures() {
		return procedures;
	}
	public void setProcedures(List<String> procedures) {
		this.procedures = procedures;
	}
	public String getModuleDocument() {
		return moduleDocument;
	}
	public void setModuleDocument(String moduleDocument) {
		this.moduleDocument = moduleDocument;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getDataSource() {
		return dataSource;
	}
	public void setDataSource(String dataSource) {
		this.dataSource = dataSource;
	}
	public String getModulePath() {
		return modulePath;
	}
	public void setModulePath(String modulePath) {
		this.modulePath = modulePath;
	}
	
}
