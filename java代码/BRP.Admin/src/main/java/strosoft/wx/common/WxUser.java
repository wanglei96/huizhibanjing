package strosoft.wx.common;

import java.util.ArrayList;

public class WxUser {
	private String subscribe;

	public String getSubscribe() {
		return subscribe;
	}

	public void setSubscribe(String subscribe) {
		this.subscribe = subscribe;
	}

	public String getOpenID() {
		return OpenID;
	}

	public void setOpenID(String openid) {
		this.OpenID = openid;
	}

	public String getNickname() {
		return Nickname;
	}

	public void setNickname(String nickname) {
		this.Nickname = nickname;
	}

	public String getSex() {
		return Sex;
	}

	public void setSex(String sex) {
		this.Sex = sex;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getCity() {
		return City;
	}

	public void setCity(String city) {
		this.City = city;
	}

	public String getProvince() {
		return Province;
	}

	public void setProvince(String province) {
		this.Province = province;
	}

	public String getCountry() {
		return Country;
	}

	public void setCountry(String country) {
		this.Country = country;
	}

	public String getHeadImgUrl() {
		return HeadImgUrl;
	}

	public void setHeadImgUrl(String headimgurl) {
		this.HeadImgUrl = headimgurl;
	}

	public String getSubscribeTime() {
		return SubscribeTime;
	}

	public void setSubscribeTime(String subscribe_time) {
		this.SubscribeTime = subscribe_time;
	}

	public String getUnionID() {
		return UnionID;
	}

	public void setUnionID(String unionID) {
		UnionID = unionID;
	}

	public ArrayList<String> getPrivileges() {
		return Privileges;
	}

	public void setPrivileges(ArrayList<String> privileges) {
		Privileges = privileges;
	}

	private String OpenID;
	private String Nickname;
	private String Sex;
	private String language;
	private String City;
	private String Province;
	private String Country;
	private String HeadImgUrl;
	private String SubscribeTime;
	private String UnionID;
	public ArrayList<String> Privileges;
}
