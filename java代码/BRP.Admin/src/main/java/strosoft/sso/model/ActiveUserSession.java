package strosoft.sso.model;

import java.util.Date;
import java.util.UUID;

import strosoft.app.data.DataEntity;

public class ActiveUserSession extends DataEntity{
	private String activeUserSessionID;
	private String accountName;
	private Date loginTime;
	private String iPAddress;
	private Date activeTime;
	public Date getLoginTime() {
		return loginTime;
	}
	public void setLoginTime(Date loginTime) {
		this.loginTime = loginTime;
	}
	public String getIPAddress() {
		return iPAddress;
	}
	public void setIPAddress(String iPAddress) {
		this.iPAddress = iPAddress;
	}
	public Date getActiveTime() {
		return activeTime;
	}
	public void setActiveTime(Date activeTime) {
		this.activeTime = activeTime;
	}
	public String getActiveUserSessionID() {
		return activeUserSessionID;
	}
	public void setActiveUserSessionID(String activeUserSessionID) {
		this.activeUserSessionID = activeUserSessionID;
	}
	public String getAccountName() {
		return accountName;
	}
	public void setAccountName(String accountName) {
		this.accountName = accountName;
	}
	
}
