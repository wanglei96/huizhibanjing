package strosoft.cms.data;

import java.util.HashMap;

public class CmsVideoContext {
	
	private String channelIndex;
	private String channelName;
	private String height;
	private String imageUrl;
	private String isAutoPlay;
	
	private String isControls;
	private String isLoop;
	private String isPreload;
	private String parent;
	private String playUrl;
	
	private String topLevel;
	private String type;
	private String upLevel;
	private String width;
	public CmsVideoContext(HashMap<String, String> attributeCondition ) {
		
		this.channelIndex=attributeCondition.get("channelIndex".toLowerCase());
		this.channelName=attributeCondition.get("channelName".toLowerCase());
		this.height=attributeCondition.get("height".toLowerCase());
		this.imageUrl=attributeCondition.get("imageUrl".toLowerCase());
		if(attributeCondition.get("isAutoPlay".toLowerCase())==null){
			this.isAutoPlay="";
		}
		else{
		this.isAutoPlay=attributeCondition.get("isAutoPlay".toLowerCase());
		}
		if(attributeCondition.get("isControls".toLowerCase())==null){
			this.isControls="";
		}else{	
		this.isControls=attributeCondition.get("isControls".toLowerCase());
		}
		this.isLoop=attributeCondition.get("isLoop".toLowerCase());
		this.isPreload=attributeCondition.get("isPreload".toLowerCase());
		this.parent=attributeCondition.get("parent".toLowerCase());
		this.playUrl=attributeCondition.get("playUrl".toLowerCase());
		this.topLevel=attributeCondition.get("topLevel".toLowerCase());
		this.type=attributeCondition.get("type".toLowerCase());
		this.upLevel=attributeCondition.get("upLevel".toLowerCase());
		this.width=attributeCondition.get("width".toLowerCase());
		attributeCondition.remove("altSrc".toLowerCase());
		attributeCondition.remove("channelIndex".toLowerCase());
		attributeCondition.remove("channelName".toLowerCase());
		attributeCondition.remove("height".toLowerCase());
		attributeCondition.remove("imageUrl".toLowerCase());
		attributeCondition.remove("isAutoPlay".toLowerCase());
		attributeCondition.remove("isControls".toLowerCase());
		attributeCondition.remove("isLoop".toLowerCase());
		attributeCondition.remove("isPreload".toLowerCase());
		attributeCondition.remove("parent".toLowerCase());
		attributeCondition.remove("playUrl".toLowerCase());
		attributeCondition.remove("topLevel".toLowerCase());
		attributeCondition.remove("type".toLowerCase());
		attributeCondition.remove("upLevel".toLowerCase());
		attributeCondition.remove("width".toLowerCase());
		
		
	}
	public CmsVideoContext() {
		// TODO Auto-generated constructor stub
	}
	
	
	
	public String getChannelIndex() {
		return channelIndex;
	}
	public void setChannelIndex(String channelIndex) {
		this.channelIndex = channelIndex;
	}
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public String getIsAutoPlay() {
		return isAutoPlay;
	}
	public void setIsAutoPlay(String isAutoPlay) {
		this.isAutoPlay = isAutoPlay;
	}
	public String getIsControls() {
		return isControls;
	}
	public void setIsControls(String isControls) {
		this.isControls = isControls;
	}
	public String getIsLoop() {
		return isLoop;
	}
	public void setIsLoop(String isLoop) {
		this.isLoop = isLoop;
	}
	public String getIsPreload() {
		return isPreload;
	}
	public void setIsPreload(String isPreload) {
		this.isPreload = isPreload;
	}
	public String getParent() {
		return parent;
	}
	public void setParent(String parent) {
		this.parent = parent;
	}
	public String getPlayUrl() {
		return playUrl;
	}
	public void setPlayUrl(String playUrl) {
		this.playUrl = playUrl;
	}
	public String getTopLevel() {
		return topLevel;
	}
	public void setTopLevel(String topLevel) {
		this.topLevel = topLevel;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getUpLevel() {
		return upLevel;
	}
	public void setUpLevel(String upLevel) {
		this.upLevel = upLevel;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	
	
	

	
}
